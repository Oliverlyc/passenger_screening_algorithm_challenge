def read_data(infile):
    """Read any of the 4 types of image files, returns a numpy array of the image contents
    """
    extension = os.path.splitext(infile)[1]
    h = read_header(infile)
    nx = int(h['num_x_pts'])
    ny = int(h['num_y_pts'])
    nt = int(h['num_t_pts'])
    fid = open(infile, 'rb')
    fid.seek(512) #skip header
    if extension == '.aps' or extension == '.a3daps':
        if(h['word_type']==7): #float32
            data = np.fromfile(fid, dtype = np.float32, count = nx * ny * nt)
        elif(h['word_type']==4): #uint16
            data = np.fromfile(fid, dtype = np.uint16, count = nx * ny * nt)
        data = data * h['data_scale_factor'] #scaling factor
        data = data.reshape(nx, ny, nt, order='F').copy() #make N-d image
    elif extension == '.a3d':
        if(h['word_type']==7): #float32
            data = np.fromfile(fid, dtype = np.float32, count = nx * ny * nt)
        elif(h['word_type']==4): #uint16
            data = np.fromfile(fid, dtype = np.uint16, count = nx * ny * nt)
        data = data * h['data_scale_factor'] #scaling factor
        data = data.reshape(nx, nt, ny, order='F').copy() #make N-d image
    elif extension == '.ahi':
        data = np.fromfile(fid, dtype = np.float32, count = 2* nx * ny * nt)
        data = data.reshape(2, ny, nx, nt, order='F').copy()
        real = data[0,:,:,:].copy()
        imag = data[1,:,:,:].copy()
    fid.close()
    if extension != '.ahi':
        return data
    else:
        return real, imag


def get_train_labels():
    with read_input_dir('data'):
        lines = open('stage1_labels.csv').readlines()[1:]

    ret = {}
    for line in lines:
        file, label = line.split(',')
        file, zone = file.split('_')
        zone = int(zone.replace('Zone', ''))
        label = int(label)

        if file not in ret:
            ret[file] = [0] * 17
        ret[file][zone-1] = label

    return ret


@cached(version=1)
def get_train_headers(filetype):
    assert filetype in ('a3d', 'aps')
    if os.path.exists('headers.pickle'):
        with open('headers.pickle', 'rb') as f:
            return pickle.load(f)
    else:
        with read_input_dir('data/%s' % filetype):
            headers = {file.split('.')[0]: read_header(file) for file in glob.glob('*')}
        with open('headers.pickle', 'wb') as f:
            pickle.dump(headers, f)
        return headers


def _get_data_generator(filetype, keep):
    assert filetype in ('a3d', 'aps')

    loc = 'data/%s' % filetype

    with read_input_dir(loc):
        files = sorted(glob.glob('*'))
        random.seed(0)
        random.shuffle(files)
    files = [file for i, file in enumerate(files) if keep(i, file.split('.')[0])]

    def gen():
        for file in tqdm.tqdm(files):
            with read_input_dir(loc):
                data = read_data(file)
            yield file.split('.')[0], data

    return gen


@cached(version=5)
def get_all_data_generator(mode, filetype):
    assert mode in ('sample', 'all')

    labels = get_train_labels()
    keep = lambda i, x: x in labels and (mode != 'sample' or i < 10)
    return _get_data_generator(filetype, keep)


@cached(version=5)
def get_train_data_generator(mode, filetype):
    assert mode in ('train', 'valid', 'sample_train', 'sample_valid')

    labels = get_train_labels()
    if mode.startswith('sample'):
        keep = lambda i, x: x in labels and i < 10 and ((i < 8) == (mode == 'sample_train'))
    else:
        keep = lambda i, x: x in labels and ((i < 800) == (mode == 'train'))
    return _get_data_generator(filetype, keep)


@cached(version=5)
def get_test_data_generator(mode, filetype):
    assert mode in ('test', 'sample_test')

    labels = get_train_labels()
    keep = lambda i, x: x not in labels and (mode != 'sample_test' or i < 100)
    return _get_data_generator(filetype, keep)


def write_answer_csv(ans_dict):
    with open('ans.csv', 'w') as f:
        f.write('Id,Probability\n')
        for label, ret in ans_dict.items():
            for i in range(17):
                f.write('%s_Zone%s,%s\n' % (label, i+1, ret[i]))
